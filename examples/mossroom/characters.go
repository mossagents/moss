// ch 是构建 CharacterDef 的辅助函数。
func ch(name, avatar, intro, persona string, themes []string, gender string) CharacterDef {
	return CharacterDef{
		Name:    name,
		Avatar:  avatar,
		Intro:   intro,
		Persona: persona,
		Themes:  themes,
		Gender:  gender,
	}
}

// CharactersForTheme 返回指定主题下的所有角色。
func CharactersForTheme(themeID string) []CharacterDef {
	var result []CharacterDef
	for i := range characterLibrary {
		for _, t := range characterLibrary[i].Themes {
			if t == themeID {
				result = append(result, characterLibrary[i])
				break
			}
		}
	}
	return result
}

// CharacterByName 根据名字查找角色。
func CharacterByName(name string) (CharacterDef, bool) {
	for _, c := range characterLibrary {
		if c.Name == name {
			return c, true
		}
	}
	return CharacterDef{}, false
}
